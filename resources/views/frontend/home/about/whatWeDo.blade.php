            <h2><a href=""> What We Do </a></h2>
            <hr>
            <p style="text-align:justify;">
            <blockquote style="margin: 0 0 0 40px; border: none; padding: 0px;">Human rights are rights inherent to all human beings,
                 regardless of race, sex, nationality, ethnicity,
                  language, religion, or any other status. Human
                   rights include the right to life and liberty,
                    freedom from slavery and torture, freedom of
                     opinion and expression, the right to work and 
                     education, and many more. Everyone is entitled
                      to these rights, without discrimination.&nbsp;
                      <br></blockquote>
            <blockquote style="margin: 0 0 0 40px; border: none; padding: 0px;">
                <div><br></div>
                <div>
                    <div><b>
                            <font face="arial" style="">NHRCCB 
                                works in over 29 Indian states 
                                and territories to Protection an
                                d Promotion of human rights and 
                                crime prevention . And we never 
                                give up.&nbsp;</font>
                        </b></div>
                </div>
                <div>
                    <div><br></div>
                </div>
                <div>
                    <div><strong class="lead">Six fundamental rights recognized in the Constitution of India: -</strong></div>
                    <hr>
                </div>
                <div><b><br></b></div>
                <div>
                    <div><strong>1.&nbsp;&nbsp;</strong>Right to Equality, which is subject to equality for equals.<br></div>
                </div>
                <div>
                    <div><b>2.&nbsp;</b> Right to Freedom, which includes Freedom of Speech and Expression, Right to Assemble Peacefully, Freedom to form Associations or Unions, Right&nbsp; &nbsp; &nbsp; &nbsp;to Move Freely throughout the territory of India, Right to Reside or Settle in any part of the territory of India, Right to Practice any profession or to&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; carry on any occupation.&nbsp;<br></div>
                </div>
                <div>
                    <div><b>3.&nbsp;</b>Right against Exploitation&nbsp;</div>
                    <div><b>4</b>. Right to Freedom of Religion<br></div>
                </div>
                <div>
                    <div><b>5.&nbsp;</b>Cultural and Educational rights<br></div>
                </div>
                <div>
                    <div><b>6.&nbsp;</b>Right to Constitutional Remedies&nbsp;<br></div>
                </div>
                <div>
                    <div><br></div>
                </div>
                <div>
                    <div><b>&nbsp;We works for human rights protection Act 1993 Indian constitution of India

                            The Universal Deceleration of Human Rights United Nation 1948 :</b></div>
                </div>
                <div>
                    <div><br></div>
                </div>
                <div>
                    <div><b>

                            Article 1&nbsp;</b></div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; All human beings are born free and equal in dignity and rights. They are endowed with reason and conscience and should act towards one another&nbsp; &nbsp; &nbsp; &nbsp; in&nbsp; &nbsp; &nbsp; a spirit of brotherhood.&nbsp;</div>
                    <div>&nbsp;<b>Article 2</b></div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp;Everyone is entitled to all the rights and freedoms set forth in this Declaration, without distinction of any kind, such as race, colour, sex, language,&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;religion, political or other opinion, national or social origin, property, birth or other status. Furthermore, no distinction shall be made on the basis of&nbsp; &nbsp; &nbsp; &nbsp;the political, jurisdictional or international status of the country or territory to which a person belongs, whether it be independent, trust, non-self-&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;governing or under any other limitation of sovereignty.&nbsp;</div>
                </div>
                <div>
                    <div><b>Article 3&nbsp;</b></div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp; Everyone has the right to life, liberty and security of person.</div>
                </div>
                <div>
                    <div><b>&nbsp;Article 4</b>&nbsp;</div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp; &nbsp;No one shall be held in slavery or servitude; slavery and the slave trade shall be prohibited in all their forms.</div>
                </div>
                <div>
                    <div>&nbsp;

                        <b>Article 5</b>&nbsp;
                    </div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp; &nbsp;No one shall be subjected to torture or to cruel, inhuman or degrading treatment or punishment.&nbsp;</div>
                </div>
                <div>
                    <div>&nbsp;
                        <b>Article 6</b>
                    </div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp; &nbsp;Everyone has the right to recognition everywhere as a person before the law.</div>
                </div>
                <div>
                    <div>&nbsp;

                        <b>Article 7</b>&nbsp;
                    </div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp; &nbsp;All are equal before the law and are entitled without any discrimination to equal protection of the law. All are entitled to equal protection against&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;any discrimination in violation of this Declaration and against any incitement to such discrimination.&nbsp;</div>
                </div>
                <div>
                    <div>&nbsp;
                        <b>Article 8</b>&nbsp;
                    </div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp; &nbsp; Everyone has the right to an effective remedy by the competent national tribunals for acts violating the fundamental rights granted him by the&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; constitution or by law.&nbsp;</div>
                </div>
                <div>
                    <div>&nbsp;
                        <b>Article 9</b>&nbsp;
                    </div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp; &nbsp;No one shall be subjected to arbitrary arrest, detention or exile.&nbsp;</div>
                </div>
                <div>
                    <div>&nbsp;
                        <b>Article 10&nbsp;</b>
                    </div>
                    <div>&nbsp; &nbsp; &nbsp; &nbsp;Everyone is entitled in full equality to a fair and public hearing by an independent and impartial tribunal, in the determination of his rights and&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;obligations and of any criminal charge against him.&nbsp;</div>
                </div>
                <div>
                    <div>&nbsp;
                        <b>Article 11</b>&nbsp;
                    </div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp; &nbsp;Everyone charged with a penal offence has the right to be presumed innocent until proved guilty according to law in a public trial at which he has&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;had all the guarantees necessary for his defence. No one shall be held guilty of any penal offence on account of any act or omission which did not&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;constitute a penal offence, under national or international law, at the time when it was committed. Nor shall a heavier penalty be imposed than the&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;one that was applicable at the time the penal offence was committed.&nbsp;</div>
                </div>
                <div>
                    <div>&nbsp;
                        <b>Article 12</b>&nbsp;
                    </div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp; &nbsp;No one shall be subjected to arbitrary interference with his privacy, family, home or correspondence, nor to attacks upon his honour and&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;reputation.&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Everyone has the right to the protection of the law against such interference or attacks.</div>
                </div>
                <div>
                    <div>&nbsp;

                        <b>Article 13</b>&nbsp;
                    </div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp; &nbsp;Everyone has the right to freedom of movement and residence within the borders of each state.
                        Everyone has the right to leave any country,&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;including his own, and to return to his country.&nbsp;</div>
                </div>
                <div>
                    <div>&nbsp;
                        <b>Article 14&nbsp;</b>
                    </div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp; &nbsp;Everyone has the right to seek and to enjoy in other countries asylum from persecution. This right may not be invoked in the case of prosecutions&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;genuinely arising from non-political crimes or from acts contrary to the purposes and principles of the United Nations.&nbsp;</div>
                </div>
                <div>
                    <div>&nbsp;
                        <b>Article 15</b>&nbsp;
                    </div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp; &nbsp; Everyone has the right to a nationality. No one shall be arbitrarily deprived of his nationality nor denied the right to change his nationality.&nbsp;</div>
                </div>
                <div>
                    <div>&nbsp;
                        <b>Article 16&nbsp;</b>
                    </div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp; &nbsp; Men and women of full age, without any limitation due to race, nationality or religion, have the right to marry and to found a family. They are&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; entitled to equal rights as to marriage, during marriage and at its dissolution. Marriage shall be entered into only with the free and full consent of&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; the intending spouses. The family is the natural and fundamental group unit of society and is entitled to protection by society and the State.&nbsp;</div>
                </div>
                <div>
                    <div>&nbsp;
                        <b>Article 17</b>&nbsp;
                    </div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Everyone has the right to own property alone as well as in association with others. No one shall be arbitrarily deprived of his property.&nbsp;</div>
                </div>
                <div>
                    <div>&nbsp;
                        <b>Article 18</b>&nbsp;
                    </div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Everyone has the right to freedom of thought, conscience and religion; this right includes freedom to change his religion or belief, and freedom,&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; either alone or in community with others and in public or private, to manifest his religion or belief in teaching, practice, worship and observance.&nbsp;</div>
                </div>
                <div>
                    <div>&nbsp;
                        <b>Article 19</b>
                    </div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Everyone has the right to freedom of opinion and expression; this right includes freedom to hold opinions without interference and to seek,&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; receive and impart information and ideas through any media and regardless of frontiers.&nbsp;</div>
                </div>
                <div>
                    <div>&nbsp;
                        <b>Article 20</b>&nbsp;
                    </div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Everyone has the right to freedom of peaceful assembly and association. No one may be compelled to belong to an association.&nbsp;</div>
                </div>
                <div>
                    <div>&nbsp;
                        <b>Article 21</b>
                    </div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Everyone has the right to take part in the government of his country, directly or through freely chosen representatives. Everyone has the right of&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; equal access to public service in his country.
                        The will of the people shall be the basis of the authority of government; this will shall be expressed&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; in periodic and genuine elections which shall be by universal and equal suffrage and shall be held by secret vote or by equivalent free voting&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; procedures.&nbsp;</div>
                </div>
                <div>
                    <div>&nbsp;
                        <b>Article 22&nbsp;</b>
                    </div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Everyone, as a member of society, has the right to social security and is entitled to realization, through national effort and international co-&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;operation and in accordance with the organization and resources of each State, of the economic, social and cultural rights indispensable for his&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;dignity and the free development of his personality.&nbsp;</div>
                </div>
                <div>
                    <div>&nbsp;
                        <b>Article 23</b>
                    </div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Everyone has the right to work, to free choice of employment, to just and favorable conditions of work and to protection against unemployment.&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Everyone, without any discrimination, has the right to equal pay for equal work.

                        Everyone who works has the right to just and favorable&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;remuneration ensuring for himself and his family an existence worthy of human dignity, and supplemented, if necessary, by other means of&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;social protection.

                        Everyone has the right to form and to join trade unions for the protection of his interests.&nbsp;</div>
                </div>
                <div>
                    <div><b>Article 24</b>&nbsp;</div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Everyone has the right to rest and leisure, including reasonable limitation of working hours and periodic holidays with pay.&nbsp;</div>
                </div>
                <div>
                    <div><b>Article 25</b></div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Everyone has the right to a standard of living adequate for the health and well-being of himself and of his family, including food, clothing,&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; housing and medical care and necessary social services, and the right to security in the event of unemployment, sickness, disability, widowhood,&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; old age or other lack of livelihood in circumstances beyond his control.
                        Motherhood and childhood are entitled to special care and assistance.&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;All children, whether born in or out of wedlock, shall enjoy the same social protection.&nbsp;</div>
                    <div>&nbsp;<b>Article 26</b>&nbsp;</div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; Everyone has the right to education. Education shall be free, at least in the elementary and fundamental stages. Elementary education shall be&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;compulsory. Technical and professional education shall be made generally available and higher education shall be equally accessible to all on&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; the basis of merit. Education shall be directed to the full development of the human personality and to the strengthening of respect for human&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; rights and fundamental freedoms. It shall promote understanding, tolerance and friendship among all nations, racial or religious groups, and&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; shall further the activities of the United Nations for the maintenance of peace. Parents have a prior right to choose the kind of education that&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; shall be given to their children.&nbsp;</div>
                </div>
                <div>
                    <div><b>Article 27</b>&nbsp;</div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Everyone has the right freely to participate in the cultural life of the community, to enjoy the arts and to share in scientific advancement and its&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;benefits.
                        Everyone has the right to the protection of the moral and material interests resulting from any scientific, literary or artistic production of&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;which he is the author.&nbsp;</div>
                </div>
                <div>
                    <div>&nbsp;
                        <b>Article 28</b>&nbsp;
                    </div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Everyone is entitled to a social and international order in which the rights and freedoms set forth in this Declaration can be fully realized.&nbsp;</div>
                </div>
                <div>
                    <div>&nbsp;
                        <b>Article 29</b>&nbsp;
                    </div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Everyone has duties to the community in which alone the free and full development of his personality is possible.In the exercise of his rights and&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;freedoms, everyone shall be subject only to such limitations as are determined by law solely for the purpose of securing due recognition and&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;respect for the rights and freedoms of others and of meeting the just requirements of morality, public order and the general welfare in a&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;democratic society .These rights and freedoms may in no case be exercised contrary to the purposes and principles of the United Nations.&nbsp;</div>
                </div>
                <div>
                    <div>&nbsp;
                        <b>Article 30</b>&nbsp;
                    </div>
                </div>
                <div>
                    <div>&nbsp; &nbsp; &nbsp; &nbsp; Nothing in this Declaration may be interpreted as implying for any State, group or person any right to engage in any activity or to perform any act&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; aimed at the destruction of any of the rights and freedoms set forth herein.
                    </div>
                </div>
            </blockquote>
